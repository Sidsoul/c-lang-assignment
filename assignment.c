// Program of Assignment operators

#include <stdio.h>

int main()
{

    
    int a = 100;
    printf("a = 100: %d\n", a);

    
    a += 10;
    printf("a += 10: %d\n", a);

    
    a -= 10;
    printf("a -= 10: %d\n", a);

    
    a *= 10;
    printf("a *= 10: %d\n", a);

    
    a /= 10;
    printf("a /= 10: %d\n", a);


    a %= 10;
    printf("a %= 10: %d\n", a);
    

    return 0;
}
