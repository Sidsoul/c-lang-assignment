// Program of Assignment operators

#include <stdio.h>

int main()
{

    
    int a = 100;
    printf("Value of a is %d\n", a);

    
    a += 10;
    printf("Value of a is %d\n", a);

    
    a -= 10;
    printf("Value of a is %d\n", a);

    
    a *= 10;
    printf("Value of a is %d\n", a);

    
    a /= 10;
    printf("Value of a is %d\n", a);

    return 0;
}
