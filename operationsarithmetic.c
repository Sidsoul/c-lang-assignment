//arithmetic operations
#include <stdio.h>
int main(){
    int a = 100, b = 50;
    int sum, sub, mult, div, mod;
    sum = a+b;
    sub = a-b;
    mult = a*b;
    div = a/b;
    mod = a%b;
    printf("Sum = %d\n",sum);
    printf("Subtraction = %d\n",sub);
    printf("Product = %d\n",mult);
    printf("Division = %d\n",div);
    printf("Modulus = %d\n",mod);
    return 0;

}
