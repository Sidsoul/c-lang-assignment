// Program of bitwise operators

#include <stdio.h>
int main()
{
    int x = 3, y = 8;

    
    printf("x = %d, y = %d\n", x, y);
    printf("x&y = %d\n", x & y);

    
    printf("x|y = %d\n", x | y);

    
    printf("x^y = %d\n", x ^ y);

    
    
    printf("~x = %d\n", x = ~x);

    
    printf("y<<1 = %d\n", y << 1);

    
    printf("y>>1 = %d\n", y >> 1);

    return 0;
}
